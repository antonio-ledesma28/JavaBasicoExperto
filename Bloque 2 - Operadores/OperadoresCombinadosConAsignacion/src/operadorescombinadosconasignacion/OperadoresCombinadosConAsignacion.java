
package operadorescombinadosconasignacion;

public class OperadoresCombinadosConAsignacion {

    public static void main(String[] args) {
        int numero = 10;
        
        numero += 5;// numero = numero + 5;
        numero -= 5; // numero = numero - 5;
        numero *=5; // numero = numero * 5;
        numero /=5; //numero = numero / 5;
        numero %= 5; //numero = numero % 5;
        System.out.println(numero); 
    }
    
}
